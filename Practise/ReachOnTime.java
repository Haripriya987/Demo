import java.util.*;

class ReachOnTime
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner in=new Scanner(System.in);
		int n=in.nextInt();
		
		while(n-->0){
		  int z=in.nextInt();  
		if(z>=30)
		System.out.println("Yes");
		else
		System.out.println("No");
		}

	}
}
